// search-box 
const search = document.getElementById("search-inputt");
const showItems = document.querySelector("#showItems");
let search_term = "";

const showList = () => {
  showItems.innerHTML = "";
  product.filter((item) => {
    return (
      item.title.toLowerCase().includes(search_term)
    );
  }).forEach((e) => {
    showItems.innerHTML += `<a href="#" class="list-group-item search-item border-0 pb-0 p-3">
    <img src="${e.img[0]}" class="img-search-sh " alt="">
    <span class="search-show-title fs-13 fw-normal">${e.title}</span>
    <div class="py-0 text-start">
            <span class="fw-medium fs-14 f-vazir-fanum">
            <del aria-hidden="true" class="text-danger ">
            <span><bdi>${e.price}&nbsp;</bdi>
            </span></del>
            <span class="">۹۹۹,۰۰۰&nbsp;<span class="">تومان</span></span></span> 
    </div>`
  });
};
search.addEventListener("keyup", (event) => {
  showItems.style.display = "block";
  event.preventDefault();
  if(search.value){
    showItems.innerHTML = "";
    search_term = event.target.value.toLowerCase();
    showItems.innerHTML = showList();
    showList();
  }else if(search.value === '') {
    showItems.style.display = "none";
  }
//   console.log(search.value)
});

